# ── Log cleanup ───────────────────────────────────────────────────────────────

def cleanup_project_logs(project: str, pipeline_days: int = 14, history_days: int = 30) -> int:
    """Delete stale per-project log files.

    - logs/pipeline/*.log  older than pipeline_days
    - logs/history_*.jsonl older than history_days

    Returns count of deleted files.
    """
    import time
    deleted = 0
    now = time.time()

    pipe_dir = pipeline_logs_dir(project)
    if pipe_dir.exists():
        cutoff = now - pipeline_days * 86400
        for f in pipe_dir.glob("*.log"):
            try:
                if f.stat().st_mtime < cutoff:
                    f.unlink()
                    deleted += 1
            except OSError:
                pass

    hist_d = history_dir(project)
    if hist_d.exists():
        cutoff = now - history_days * 86400
        for f in hist_d.glob("history_*.jsonl"):
            try:
                if f.stat().st_mtime < cutoff:
                    f.unlink()
                    deleted += 1
            except OSError:
                pass

    return deleted


# ── Directory creation helpers ────────────────────────────────────────────────

def ensure_project_dirs(project: str) -> None:
    """Create all required project directories if they don't exist."""
    for d in [
        memory_dir(project),
        claude_dir(project),
        claude_dir(project) / "features",
        cursor_dir(project),
        copilot_dir(project),
        api_dir(project),
        sessions_dir(project),
        state_dir(project),
        history_dir(project),
        pipelines_dir(project),
        runs_dir(project),
    ]:
        d.mkdir(parents=True, exist_ok=True)
