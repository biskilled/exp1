"""
project_paths.py — Single source of truth for all workspace file paths.

All backend code should import path helpers from here instead of
constructing paths inline. This makes folder structure changes a
one-file edit.

Workspace layout (per project):
  workspace/{project}/
    memory/                 ← LLM context files (auto-generated by /memory)
      claude/
        CLAUDE.md           ← copied to code_dir/CLAUDE.md
        features/           ← per-feature snapshots
        hooks/              ← hook scripts
      cursor/rules.md       ← compact, copied to code_dir/.cursorrules
      copilot/instructions.md ← compact (= cursor/rules.md), → code_dir/.github/copilot-instructions.md
      api/system_prompt.md  ← compact, shared by all API providers (Claude/OpenAI/Gemini/Grok/DeepSeek)
      context.md            ← injected by aicli CLI into ALL providers
      code.md               ← directory tree + hotspots + file coupling
      GEMINI.md             ← full (= CLAUDE.md), copied to code_dir/GEMINI.md
      AGENTS.md             ← full (= CLAUDE.md), copied to code_dir/AGENTS.md
      memory.yaml           ← maps all CLI tools + APIs to their context files
      _mcp.json             ← Claude Code / Cursor MCP config template
      _codex.toml           ← Codex CLI MCP config template
      _opencode.json        ← OpenCode config template
    state/                  ← runtime state files
      project_state.json
      session_tags.json
      session_phases.json
      dev_runtime_state.json
      CONTEXT.md
      .git_token
    history/                ← append-only history logs
      history.jsonl         ← conversation history (all prompts)
      commit_log.jsonl      ← git commit log
      history_*.jsonl       ← rotated history archives
    sessions/               ← per-session JSON files (LLM message continuity)
    pipelines/              ← pipeline definitions, prompts, and run results
      samples/              ← sample pipeline YAML files
      prompts/              ← pipeline prompt files
      runs/                 ← pipeline run result JSONs
    documents/              ← user-managed docs (use cases, references)
"""
from __future__ import annotations

from pathlib import Path

from core.config import settings


